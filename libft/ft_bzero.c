#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	if (!s || n < i)
		return;
	while (i < n && (char *)(s + i))
	{
		((char * )s)[i] = 0;
		i++;
	}	
}
